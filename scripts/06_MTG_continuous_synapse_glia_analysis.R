# -------------------------
# Correlation and regression analysis
# -------------------------
run_continuous_analysis <- function(df, y_col, label) {
  cor_res <- cor.test(
    df$EpilepsyScore,
    df[[y_col]],
    method = "spearman",
    exact = FALSE
  )
  
  fit <- lm(
    as.formula(paste0(y_col, " ~ EpilepsyScore")),
    data = df
  )
  
  sm <- summary(fit)$coefficients
  ci <- confint(fit)
  
  data.frame(
    Comparison = paste0("EpilepsyScore vs ", label),
    Spearman_rho = unname(cor_res$estimate),
    Spearman_P_value = cor_res$p.value,
    Linear_regression_beta = sm["EpilepsyScore", "Estimate"],
    Linear_regression_P_value = sm["EpilepsyScore", "Pr(>|t|)"],
    CI_lower_for_beta = ci["EpilepsyScore", 1],
    CI_upper_for_beta = ci["EpilepsyScore", 2],
    stringsAsFactors = FALSE
  )
}

s12_table <- rbind(
  run_continuous_analysis(module_df, "SynapseScore", "synaptic module score"),
  run_continuous_analysis(module_df, "GliaScore", "glia-associated module score"),
  run_continuous_analysis(module_df, "SynGliaBalance", "synapse-glia balance score")
)

write.csv(
  s12_table,
  file = file.path(dir_tables, "Supplementary_Table_S12_MTG_continuous_module_analysis.csv"),
  row.names = FALSE
)

# -------------------------
# Plotting function
# -------------------------
plot_continuous <- function(df, y_col, y_label, title_text, out_file) {
  p <- ggplot(
    df,
    aes(x = EpilepsyScore, y = .data[[y_col]])
  ) +
    geom_point(size = 2.6, alpha = 0.85) +
    geom_smooth(method = "lm", se = TRUE, color = "black", linewidth = 0.7) +
    labs(
      title = title_text,
      x = "EpilepsyScore",
      y = y_label
    ) +
    theme_bw(base_size = 13) +
    theme(
      panel.grid.minor = element_blank(),
      plot.title = element_text(hjust = 0.5)
    )
  
  ggsave(
    filename = out_file,
    plot = p,
    width = 5.5,
    height = 4.5,
    dpi = 300
  )
  
  return(p)
}

